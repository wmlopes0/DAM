    public ArrayList<Integer> getAdyacentes(int fila, int columna) {
        ArrayList<Integer> adyacentes = new ArrayList<>();
        int idCelda;

        /*Con este doble for, recorro el cuadrado de 3 * 3 que rodea la posicion indicada por parametro
        comprobando en cada iteracción si la posicion dada se encuentra dentro de la matriz y que no es la
        posicion que le pasamos por parámetro, pues esa es la posición en la que se encontraría el robot*/
        for (int f = (fila - 1); f <= (fila + 1); f++) {
            for (int c = (columna - 1); c <= (columna + 1); c++) {
                if (dentroMatriz(f, c) && f != fila | c != columna) {
                    idCelda = matriz[f][c].getIdCelda();
                    adyacentes.add(idCelda);
                }
            }
        }

        return adyacentes;
    }

